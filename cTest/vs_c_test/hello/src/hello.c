void show_pointer_int(const int *pIn)
{
	if (pIn != NULL)
	{
		printf("*pIn=%d\n", *pIn);
	}
	else
	{
		printf("pIn is NULL!\n");
	}
	return;
}

void test_printf()
{
	int printf = 2;//ok
	int * pInt = NULL;
	pInt = &printf;

	if (pInt != NULL)
	{
		//printf("*pInt=%d", *pInt); //error C2063: “printf”: 不是一个函数
		show_pointer_int(pInt);
	}
}
